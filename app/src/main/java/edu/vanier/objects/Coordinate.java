package edu.vanier.objects;

/**
 *
 * @author edeli
 */
public class Coordinate {
    
    private final double x;
    private final double y;

    public Coordinate(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return this.x;
    }
    
    public double getY() {
        return this.y;
    }
}
